public class Bibioteca {
    private String usuarios_biblioteca;
    private String libros_biblioteca;

    //CONS
    public Bibioteca(String usuarios_biblioteca, String libros_biblioteca) {
        this.usuarios_biblioteca = usuarios_biblioteca;
        this.libros_biblioteca = libros_biblioteca;
    }

    public String getUsuarios_biblioteca() {
        return usuarios_biblioteca;
    }

    public void setUsuarios_biblioteca(String usuarios_biblioteca) {
        this.usuarios_biblioteca = usuarios_biblioteca;
    }

    public String getLibros_biblioteca() {
        return libros_biblioteca;
    }

    public void setLibros_biblioteca(String libros_biblioteca) {
        this.libros_biblioteca = libros_biblioteca;
    }

    //G Y S
    
    
}

